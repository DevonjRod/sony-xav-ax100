#define SP_PNG_DEC_INFO_SIGNATURE   0x474e505f  // "_PNG"

#define SP_PNG_DEC_INFO_STATE_IDLE  (0)
#define SP_PNG_DEC_INFO_STATE_RUN   (1)
#define SP_PNG_DEC_INFO_STATE_DONE  (2)

typedef struct sp_png_dec_info_s {
    u32 signature;
    u32 scramble_key;
    u32 addr_src;
    u32 addr_dst;
    u32 src_size;
    u32 decompressed_size;
    u32 ZlibBlockSize;
    u32 ZlibBlockCnt;
    u32 idx_ZlibBlock;
    u32 idx_ZlibBlock_loaded;
    u32 LZ_backward_buf_phyaddr;
    u32 scanline_buf_phyaddr;
    u32 exe_state;
    PNG_INFO png_info_data;
} sp_png_dec_info_t;

static sp_png_dec_info_t sp_png_dec_info;

int sp_png_dec_run(void)
{
    int ret;
    u32 exe_mode;
    u32 i;

    for(i=0; i<3; i++) {
        if(sp_png_dec_info.signature != SP_PNG_DEC_INFO_SIGNATURE) {
            // printf("%s, invalide data\n", __func__);
            return -1;
        }

        if(sp_png_dec_info.idx_ZlibBlock >= sp_png_dec_info.idx_ZlibBlock_loaded) {
            // printf("%s, data not available yet.\n", __func__);
            return 0;
        }

        switch(sp_png_dec_info.exe_state) {
            case 0:
                sp_png_dec_info.exe_state = 1;
                exe_mode = 1;
                break;
            case 1:
                sp_png_dec_info.exe_state = 2;
                exe_mode = 2;
                break;
            case 2:
                // sp_png_dec_info.exe_state = 2; // Keep checking if h/w deocder is done.
                exe_mode = 2;
                break;
            default:
                break;
        }
        ret = imgdec_png_decode_zlib(sp_png_dec_info.idx_ZlibBlock,
                    sp_png_dec_info.ZlibBlockSize,
                    (unsigned char *)(sp_png_dec_info.addr_src + (sp_png_dec_info.idx_ZlibBlock * sp_png_dec_info.ZlibBlockSize)),  // virtual address
                       (unsigned int)(sp_png_dec_info.addr_src + (sp_png_dec_info.idx_ZlibBlock * sp_png_dec_info.ZlibBlockSize)),  // physical address
                    sp_png_dec_info.LZ_backward_buf_phyaddr,
                    sp_png_dec_info.scanline_buf_phyaddr,
                    sp_png_dec_info.addr_dst,
                    (unsigned short)(sp_png_dec_info.scramble_key),
                    &(sp_png_dec_info.png_info_data), &exe_mode);
        // printf("%s executed, idx_idx_ZlibBlock=%u\n", __func__, sp_png_dec_info.idx_ZlibBlock);
        if((sp_png_dec_info.exe_state == 2) && (exe_mode & (1 << 31))) {   // one block of decoding is done.
            sp_png_dec_info.idx_ZlibBlock++;
            sp_png_dec_info.exe_state = 0;

            if(sp_png_dec_info.idx_ZlibBlock >= sp_png_dec_info.idx_ZlibBlock_loaded)
                i = 0;  // make sure PNG_DECODER is active if data is available
            else
                i = ~0;
        }
        if(ret < 0) {
            printf("Error: imgdec_png_decode_zlib() < 0 \n");
            return -1;
        }
        if(ret > 0) {
            printf("%s: PNG_DEC done.\n", __func__);
            sp_png_dec_info.signature = 0;
            return ret;
        }

        if(sp_png_dec_info.exe_state == 1)  // just after triggering PNG_DECODER
            break;
    }

    return 0;
}

extern u32 disable_sp_serial_putc;
int load_image_accelerated_by_sp_png(char *partition_name,              // e.g., ecos, kernel
                                     u32 addr_src,                      // buffer for loading compressed data
                                     u32 addr_dst,                      // buffer to store decompressed data.
                                     u32 addr_32KB, u32 addr_256KB)     // assign a 32-KB and a 256-KB buffers for SP-PNG-DECODER as working buffers.
{
    char cmd[256];
    u8 *u8_ptr;
    u32 nand_addr;
    u32 flag_first = 0;
    u32 flag_decode_done = 0;
    static u32 nand_block_size = 0;
    static u32 nand_page_size = 0;
    u32 nand_read_size = 0;
    nand_info_t *nand;

    if(!nand_block_size) {
        nand = &nand_info[0];
        if(!nand) {
            printf("error: %s, %d\n", __FILE__, __LINE__);
            return -1;
        }
        nand_block_size = nand->erasesize;
        // printf("nand_block_size = 0x%x\n", nand_block_size);
        nand_page_size  = nand->writesize;
    }

    nand_read_size = nand_block_size * 1;
    // nand_read_size = nand_page_size * 1; // Very bad.

    memset(&sp_png_dec_info, 0, sizeof(sp_png_dec_info_t));
    sp_png_dec_info.signature       = SP_PNG_DEC_INFO_SIGNATURE;
    sp_png_dec_info.scramble_key    = 0x1234;
    sp_png_dec_info.addr_src        = addr_src;
    sp_png_dec_info.addr_dst        = addr_dst;
    sp_png_dec_info.ZlibBlockSize   = 1024;
    sp_png_dec_info.src_size        = (0xffff << 10) / 2;  // (max. decompressed size)/2 because the compression ratio is about 2.

    // Just get header only
    sprintf(cmd, "nand read 0x%x %s 0x10 && md.b 0x%x 0x10", sp_png_dec_info.addr_src, partition_name, sp_png_dec_info.addr_src);
    printf("%s\n", cmd);
    run_command(cmd, 0);

    u8_ptr = (u8 *)(sp_png_dec_info.addr_src);
    sp_png_dec_info.decompressed_size  = ((u32)(*(u8_ptr + 2))) << 18;
    sp_png_dec_info.decompressed_size |= ((u32)(*(u8_ptr + 3))) << 10;
    printf("%s, decompressed_size = %u\n", __func__, sp_png_dec_info.decompressed_size);
    if((*(u8_ptr + 0) != 'S') || (*(u8_ptr + 1) != 'P')) {
        sp_png_dec_info.signature = 0;
        printf("Invalid image for PNG_DEC\n");
        return -1;
    }

    sp_png_dec_info.ZlibBlockCnt    = (sp_png_dec_info.src_size + (sp_png_dec_info.ZlibBlockSize -1)) / sp_png_dec_info.ZlibBlockSize;
    sp_png_dec_info.LZ_backward_buf_phyaddr = addr_32KB;
    sp_png_dec_info.scanline_buf_phyaddr    = addr_256KB;

    while(1) {
        if(flag_first == 0) {
            flag_first =1;
            sprintf(cmd, "nand read 0x%x %s 0x%x", sp_png_dec_info.addr_src, partition_name, nand_read_size);
        } else {
            nand_addr = getenv_ulong("isp_addr_nand_read_next", 16, 0);
            if(!nand_addr) {
                printf("Error: %s, %d\n", __FILE__, __LINE__);
                while(1);
            }
            sprintf(cmd, "nand read 0x%x 0x%x 0x%x",
                sp_png_dec_info.addr_src + sp_png_dec_info.idx_ZlibBlock_loaded * sp_png_dec_info.ZlibBlockSize,
                nand_addr, nand_read_size);
        }
        // printf("%s\n", cmd);
        disable_sp_serial_putc = 1;     // Speed up "nand read"
        run_command(cmd, 0);
        disable_sp_serial_putc = 0;

        if(sp_png_dec_info.signature != SP_PNG_DEC_INFO_SIGNATURE) {    // Decode end at middle of previous "nand read", not this time of "nand read"
            flag_decode_done = 1;
            break;
        }
        while(sp_png_dec_info.idx_ZlibBlock != sp_png_dec_info.idx_ZlibBlock_loaded) {  // because execution time of PNG_DECODER is longer than nand-read's, make sure previous nand-read data has been decoded.
            // printf("sp_png_dec_info.idx_ZlibBlock: %u, sp_png_dec_info.idx_ZlibBlock_loaded: %u\n", sp_png_dec_info.idx_ZlibBlock, sp_png_dec_info.idx_ZlibBlock_loaded);
            if(sp_png_dec_run()) {
                flag_decode_done = 1;
                break;
            }
        }
        if(flag_decode_done)
            break;

        sp_png_dec_info.idx_ZlibBlock_loaded += nand_read_size / sp_png_dec_info.ZlibBlockSize;
    }

    return 0;
}


