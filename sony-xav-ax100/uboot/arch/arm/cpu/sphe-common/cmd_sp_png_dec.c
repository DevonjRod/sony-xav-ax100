/*
 *  Example:
 *
 *  * Generate compressed image:
 *    ./sp_gzip data_4MB.bin d4MB.sp 1234 # ./sp_gzip file_in file_out u16_scramble_key
 *  * Run on U-Boot environment:
 *    usb start && fatls usb 1 && fatload usb 1 0x1000000 d4MB.sp && md.b 0x1000000 0x80
 *    sp_png_dec 0x1000000 0x1800000 0x1234 && md.b 0x1800000 0x100 && md5sum 0x1800000 0x400000
 */

static int do_sp_png_dec(cmd_tbl_t *cmdtp, int flag, int argc, char *const argv[])
{
    u32 src_addr, dst_addr, scramble_key, src_size;
    u32 decompressed_size;
    u8 *u8_ptr;
    u32 ZlibBlockSize = 1024;
    u32 ZlibBufLen, ZlibBlockCnt, i, LZ_backward_buf_phyaddr, scanline_buf_phyaddr, decodedbuf_phyaddr_start;
    PNG_INFO png_info_data;
    int ret;
    ulong t1;

    if(argc < 4)
        return CMD_RET_USAGE;

    src_addr = simple_strtoul(argv[1], NULL, 16);
    dst_addr = simple_strtoul(argv[2], NULL, 16);
    scramble_key = simple_strtoul(argv[3], NULL, 16);
    if(argc > 4)
        src_size = simple_strtoul(argv[4], NULL, 16);
    else
        src_size = (0xffff << 10) / 2;  // (max. decompressed size)/2 because the compression ratio is about 2.
    ZlibBufLen = src_size;
    ZlibBlockCnt = (ZlibBufLen + (ZlibBlockSize -1)) / ZlibBlockSize;
    decodedbuf_phyaddr_start = dst_addr;

    u8_ptr = (u8 *)(src_addr);
    decompressed_size  = ((u32)(*(u8_ptr + 2))) << 18;
    decompressed_size |= ((u32)(*(u8_ptr + 3))) << 10;
    printf("%s, decompressed_size = %u\n", __func__, decompressed_size);

    // allocate LZ_backward buffer:
    LZ_backward_buf_phyaddr = src_addr + ZlibBlockCnt * ZlibBlockSize;  // At end of compressed image, size: 32 KB
    // allocate scanline buffer:
    scanline_buf_phyaddr = LZ_backward_buf_phyaddr + (32 << 10);        // 256 KB, end at (LZ_backward_buf_phyaddr + (32 << 10) + (256 << 10) - 1)

    t1 = get_timer(0);
    for(i=0; i<ZlibBlockCnt; i++) {
        // printf("enter imgdec_png_decode_zlib(): ZlibBlockCnt = %d \n", i);
        ret = imgdec_png_decode_zlib(i,
                    ZlibBlockSize,
                    (unsigned char *)(src_addr + (i * ZlibBlockSize)),  // virtual address
                       (unsigned int)(src_addr + (i * ZlibBlockSize)),  // physical address
                    LZ_backward_buf_phyaddr,
                    scanline_buf_phyaddr,
                    decodedbuf_phyaddr_start,
                    (unsigned short)(scramble_key),
                    &png_info_data, 0);
        if(ret < 0) {
            printf("Error: imgdec_png_decode_zlib() < 0 \n");
            break;
        }
        // printf("imgdec_png_decode_zlib() = %d\n", ret);
        // printf("exit from imgdec_png_decode_zlib(): ZlibBlockCnt = %d \n", i);
        if(ret > 0) {
            t1 = get_timer(t1);
            printf("%s, done. %lu msec elapsed.\n", __func__, t1);
            break;
        }
    }

    return 0;
}


